<script>
    $(document).ready(function () {

        $('body').on('click', '.modalproduct', function (event) {
            event.preventDefault();
            var url = $(this).attr('data-href');
            $.ajax({
                url: url,
                dataType: 'json',
                beforeSend: function () {
                    $('body').addClass('loader');
                },
                complete: function () {
                    $('body').removeClass('loader');
                },
                success: function (json) {
                    if (json['success'] && json['html']) {
                        $('#modalp').modal('show');
                        $('#modalp .modalp-body').append(json['html']);
                    }
                    if (json['error']) {
                        $("#modalp").removeClass('show');
                    }
                },
                error: function (xhr) {
                    $("#modalp").removeClass('show');
                }
            });
        });

        $("#modalp").delegate('.btn-close', 'click', function () {
            $("#modalp").removeClass('show'); //modal-backdrop
            $('body').removeClass('modal-open');
            $('.modal-backdrop').removeClass('show');
            $("#modalp .modalp-body").html('');
        });
    });
    $(document).ready(function () {
        if ($(".js-example-basic-multiple").length > 0) {
            $('.js-example-basic-multiple').select2();
        }
    })

</script>

<script>
    $(document).ready(function () {
        $('body').on('click', '.js-addcart-detail', function (event) {
            event.preventDefault();
            var url = $(this).attr('data-href');
            console.log(url)
            var nameProduct = $(this).parent().parent().parent().parent().find('.js-name-detail').html();
            $.ajax({
                url: url,
                data: {
                    _token: "{{ csrf_token() }}",
                    custom: $('.num-product').val(),
                    user: "{{auth()->user()->_id}}",
                    size: $('#size').val(),
                },
                type: 'POST',
                dataType: 'json',
                beforeSend: function () {
                    $('body').addClass('loader');
                },
                complete: function () {
                    $('body').removeClass('loader');
                },
                success: function (json) {
                    console.log(json)
                    if (json['success']) {
                        swal(nameProduct, "sepete eklendi!", "success");
                    }
                    if (json['error']) {
                        swal(nameProduct, json['error'], "error");
                    }
                },
                error: function (xhr) {
                    swal(nameProduct, "Hata!", "error");
                }
            });
        });
    });

</script>


<script>
    $(document).ready(function () {

        $(".quantity-minus").click(function () {
            minid = $(this).attr('data-id');
            quantityValue = parseInt($("#" + minid).val())
            if (quantityValue > 1) {
                quantityValue--;
                $("#" + minid).attr("value", quantityValue);
            }
        });

        $(".quantity-plus").click(function () {
            maxid = $(this).attr('data-id');
            quantityValue = parseInt($("#" + maxid).val())
            if (quantityValue < 100) {
                quantityValue++;
                $("#" + maxid).attr("value", quantityValue);
            }
        });
    });
</script>
